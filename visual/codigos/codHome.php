								<div style="background-color: #eee; padding: 10px;">
									<p style="font-size: 14px;">O primeiro passo para se organizar é ter pleno conhecimento dos seus gastos. Afinal, como você vai economizar se não sabe onde está gastando? Para isso, registre todas as suas despesas do último mês. Separando-as por categorias – como alimentação, carro, moradia, vestuário e lazer – fica mais fácil identificar onde estão concentrados os maiores gastos.</p>
