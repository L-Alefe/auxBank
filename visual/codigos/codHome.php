								<h4 style="background-color: #ef5350; padding: 5px; color: #fff;">2. Classifique seus gastos:</h4>
								<div style="background-color: #eee; padding: 10px;">
									<p style="font-size: 14px;">Aqui não estamos falando de separar os gastos entre as categorias (alimentação, transporte, etc). É fundamental que as despesas sejam classificadas em “necessárias” e “dispensáveis”. O nome dessa segunda categoria já indica o que deve acontecer: dispense o máximo de itens pertencentes a ela no mês seguinte.</p>
								</div>
								<br />
								<h4 style="background-color: #7986cb; padding: 5px; color: #fff;">3. Hora das contas!</h4>
								<div style="background-color: #eee; padding: 10px;">
									<p style="font-size: 14px;">Contabilize todos os gastos mensais e subtraia-os da renda do mês. A regrinha é básica: arredonde sempre as despesas para cima e a renda para baixo. Assim, você garante que uma pequena mudança no planejamento não comprometa por inteiro a reserva restante.</p>
								</div>
								<br />
								<h4 style="background-color: #81c784; padding: 5px; color: #fff;">4. Defina metas e orçamentos:</h4>
								<div style="background-color: #eee; padding: 10px;">
									<p style="font-size: 14px;">Depois de ter conhecimento sobre o seu custo mensal, é necessário criar metas separando seus gastos em três categorias:</p>
									<ul style="font-size: 14px;">
									   	<li>50% da renda para os gastos essenciais: todos aqueles necessários para você se manter no dia a dia, como moradia, alimentação, transporte, educação, etc.<br /></li>

									   	<li>15% da renda para prioridades financeiras: se você estiver endividado, sua prioridade será quitar as dívidas. Se não, poupar para o futuro.<br /></li>

										<li>35% da renda para estilo de vida: todos os gastos relacionados a hobbies e lazer, como academia, salão de beleza, compras no shopping, etc.</li>
									</ul>
								</div>
								<br />
								<h4 style="background-color: #fff176; padding: 5px;">5. Repense os gastos “necessários”:</h4>
								<div style="background-color: #eee; padding: 10px;">
									<p style="font-size: 14px;">É importante analisar se os gastos tidos como essenciais para a manutenção do dia a dia não estão tomando uma parcela da renda maior do que deveriam – o que pode acontecer se eles extrapolam a meta de 50% definida no item anterior.  Alguns questionamentos ajudam nessa tarefa: será que a compra mensal no supermercado é exagerada? As contas de água e luz estão dentro do padrão para o tamanho da residência e a quantidade de pessoas que as utilizam?</p>
